
package maquina;

/**
 * Tipos de combustible admisibles.
 * 
 * @author Profesor PROG
 */
public enum TipoCombustible {

    
    /**
     * Combustible para máquinas cuya fuerza motriz sea un motor a combustión de gasolina.
     */
    GASOLINA,   

    /**
     * Combustible para máquinas cuya fuerza motriz sea un motor a combustión diésel.
     */
    DIESEL,   

    /**
     * Combustible para máquinas cuya fuerza motriz sea un motor eléctrico.
     */
    ELECTRICIDAD   
}
